 					<h2>Contest Format:</h2>
 					<p>
 					<br />
 					- Round 1 is a written test. There will be 5 to 6 problems, all of which needs to be answered. Each problem will have certain objective questions, though 50% of the marks are reserved for the explanations of the solutions, irrespective of whether it is explicitly asked for. Check the problems from the <a href="resources.php#past" target="blank">past contests</a> for an idea about the problems.
 					<br /><br />
 					- There might be significant or complete overlap between the problems in the senior and junior categories of the contest. However, the grading scheme and the points for the same problem can be very different for the two categories.
 					<br /><br />
 					- The objective answers will be evaluated first; the explanations will be evaluated only for a subset of the contestants who clear a cut-off score (to be dynamically decided for each category based on the score distribution). The final rank list will be decided based on the sum of the scores of the objective part and explanations. 
 					<br /><br />
 					- <i>English</i> is the default working language for the contest. This means the questions and your answers should be in English. In case you want to take the test in some other Indian language, you can indicate this while filling up the registration form. If we have more than 5 requests for a particular Indian language, we will try our best to allow the concerned candidates to take the test in that language.
