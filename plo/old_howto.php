 			<h2>Eligibility Criteria:</h2><br />
 			<p>
 				1.	Students who are either Indian citizens or currently studying in a school in India are eligible for the contest (provided they meet the other eligibility criteria stated below). But note that we do not have any test centres abroad for non-resident citizens of India. 
 				<br /><br />
 				2.	A student is NOT eligible for competing in PLO if s/he is planning to compete or have already competed and awaiting results for the national linguistics Olympiad of any other country leading to the participation in IOL 2016.
 				<br /><br />
 				3.	Students currently studying in class 11 or 12 MUST compete in the <b>senior category</b>.
 				<br /><br />
 				4.	Students currently studying in class 9 and 10 MUST compete in the <b>junior category</b>. However see conditions 5 and 6 below.
 				<br /><br />
 				5.	Exceptionally bright students currently studying in class 6, 7 or 8 can also choose to compete in the junior category. For this, a certificate of excellence and endorsement for participation in PLO from the principal of the school have to be produced as a supporting evidence.
 				<br /><br />
 				6.	Students who have attended any of the past PLO round 2 orientation-cum-selection camp and are eligible to write the test otherwise, MUST opt for the senior category even if they study in class 10 or below.
 				<br /><br />
 				7.	Students who have qualified for or represented India in IOL 2015 are directly eligible for round 2 and the camp, and need NOT compete inPLO round 1.
 				<br /><br />
 				8.	The PLO 2016 Executive Board reserves all rights to disallow or disqualify any candidate from competing in the contest. The decision of the board on any disputes regarding the rules, eligibility and selection criteria shall be considered as final.
