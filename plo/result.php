<h1>PLO 2016 Round 1 Results</h1>
			 
			
			 	<h2>Evaluation Process:</h2>
					 <p>	1. The objective part for each student was graded first.<br/><br/>
						2. The top 35 senior and juniors (total 70 scripts) were shortlisted for evaluation of the explanations.<br/><br/>

			           3. Both the objective part and explanations were evaluated by two independent jury members<br/><br/>

			           4. For each problem, 50% weightage was assigned to the objective part, and remaining 50% on the explanation.<br/><br/>

			            5. Only for problem #6, where by random guess one could score on average 50% in the objective part (and many of you have indeed mentioned that it was a random guess; we admire your honesty.), 
			            the objective score was considered if and only if the score in the explanation part was greater than 0 <br/><br/>
			<h2>Cut-offs:</h2>
<p>
					The cut-off on the total marks (after adding the objective and explanation scores) was 35%.<br/> 
					The cut-off was same for the seniors and juniors because the performance of the students in both the categories were similar. 
					In other words, seniors did not better than juniors (that's quite impressive, juniors!). <br/><br/>

	</p>

     
