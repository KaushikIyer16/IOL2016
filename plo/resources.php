										<b>Sheep Scoring<br /></b>
										Below are Sheep Scoring numbers* from Millom, Cumbria in Northern England.<br />
										The numerals and their values are in RANDOM order.<br /><br />
										15 	aina-bumfit<br />
										1	para-dig<br />
										16	bumfit<br />
										2	aina-dig<br />
										10	aina<br />
										11	dig<br />
										14  peina<br />
										Find the value of peina-bumfit.<br /><br />*Sheep Scoring numbers were used by shepherds to count sheep, by children to play games and by ladies to count knitting stitches! 

										<b><br /><br />Simte* Verbs<br /></b>
										Below are some verb phrases from the Simte language.  Loosely translated, all the verbs mean "to wear" in English. But the Simte people use each verb in a specific way with particular objects.
										<br /><br />
										puannah silh = to wear shirt;<br />
										toka teng = to wear pants;<br />
										kengtophah but = to wear shoes.<br /><br />
										If, 'silh', 'teng' and 'but', all mean 'to wear/to put on'<br /><br />
										Then find out which verb is used for the following:<br /> 
										1) puanzaal (shawl)     2) kengmoija (socks)   3) puan (dress)<br />
										4) khutphaw (gloves)  5) zaanpuan (blanket) 6) niih-siing (skirt)<br /><br />
										*Simte is a language of the Kuki-Chin clan from the Tibeto-Burman group and is primarily spoken in Manipur in North East India.


										<b><br /><br />Maithili*<br /></b>
										Meena and Sunanda are teachers while Ram and Shyam are students. The following are dialogues amongst the four in Maithili with their English translations.
										<br /><br />
										Meena to Ram: Ham tora ganit padhay chiyo.<br />  
                                		I am teaching you mathematics.<br /><br />
                                		Sunanda to Shyam: Meena toraa ganit padhay chathun.<br />
                                        Meena is teaching you mathematics.<br /><br />
                                        Meena to Shyam: Ram toraa dekhay cho.<br />
                                    	Ram is looking at you.<br /><br />
                                    	Sunanda to Meena: Shyam ahaka dekhay ya.<br />
                                        Shyam is looking at you.<br /><br />
                                        Ram to Meena: Ham ahaka sunay chi.<br />
                               			I am listening to you.<br /><br />
                               			Shyam to Meena: Sunanda ahaka sunay chayt.<br />
                                   		Sunanda is listening to you.<br /><br />

                                   		1. Identify the person to whom the following statements are addressed-<br />
                                   		- Meena ahaka dekhay chayt.<br />
                                   		- Shyam toraa sunay cho<br /><br />

                                   		2. Translate the following sentences into English-<br />
                                   		- Ram ahaka ganit padhay ya<br />
                                   		- Ram ahaka ganit padhay ya<br /><br />

                                   		3. Translate the following sentences into Maithili.<br />
                                   		- Sundanda is looking at you.<br />
                                   		- Shyam: I am teaching you mathematics.<br />
                                   		(Hint: Each sentence will have two translations.)<br /><br />

                                   		*Maithili  is an Indo-Aryan language spoken in the Mithila region of eastern Nepal and northern India, having over 35 million native speakers.



