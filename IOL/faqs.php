							<br /><br />

							<b>1.2. What is considered as a team?</b>
							<br />
							A team consists of up to 4 contestants and one team-leader. The contestants must be students of a 
							school for secondary education or have graduated from one in the academic year 2015-2016.<i> A team 
							MUST have a team leader and at least one contestant.</i> We also recommend that NO individual
							should be the leader of more than one team.

							<br /><br />

							<b>1.3. Do I pay the same registration fee even if my team has fewer than 5 participants?</b>
							<br />
							Yes, even if there is only one team leader and one contestant, you pay the full registration fee as
							applicable, for the team.

							<br /><br />

							<b>1.4. Who are Observers? How many observers can a country send?</b>
							<br />
							Irrespective of whether they are sending a team, any country or territory recognized by the board
							can send up to TWO observers. Observers can take part in all the official IOL events, including the 
							opening and closing ceremonies, excursion and team leader’s meeting.

							<br /><br />

							<b>1.5. We have already registered one team. Can we now include a second team?</b>
							<br />
							Yes, you can add another team for your country only till 31st May 2016. If you are sure that one team
							will definitely come from your country but not sure of the second team, we would suggest you to
							register only one team for now and avail the early-bird discount. You can register the second team
							only if and when you are sure of its participation.

							<br /><br />

							<b>1.6. Can we de-register a team that I had already registered?</b>
							<br />
							Yes, you can de-register a team. Just write to us. However, we will NOT be able to refund the money
							if you have already made the payment.

							<br /><br />

							<b>1.7. Can we add observers to our team later?</b>
							<br />
							Technically, observers are not part of the team, and any country can add up to TWO observers till
							31st May 2016 (because after this date getting visa will be tight). But beware that you will incur some 
							transfer charges for every different transfer you make. It is advisable to register for all the teams and
							observers from your country in one go.

							<br /><br />

							<b>1.8. Can’t you accept the payment through credit card or some other means?</b>
							<br />
							Unfortunately, No. We spoke to a few credit-card payment gateway setup agencies. They will charge
							close to EUR 2000 for setting up the gateway, plus a 1.5-2.5% surcharge on every transaction. We 
							believe this is too high a cost for a rather insignificant benefit.

							<br /><br />

							<b>1.9. Can’t we pay in cash onsite?</b>
							<br />
							Sorry, no. We will have no cash counters and cashiers onsite. The registration process and fee
							payment must be completed online and through bank transfer by 17th June 2016.

							<br /><br />

							<b>1.10. Can’t we pay for the post-IOL excursions along with the registration fee payment?</b>
							<br />
							No, again! The post-IOL excursions are organized by a travel agent and is not technically a part 
							of the IOL budget or program. You can think of it as a special organized tour through a travel agent that we 
							planned and negotiated for you, but all the business happen between you and the travel agent. The 
							travel agent will share their bank account where the money should be transferred.

