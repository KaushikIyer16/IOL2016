                  <h2>Registration Process</h2>
                  <p>
                     Online registration is mandatory for every country intending to participate in IOL 2016.
                     We will use a conference management system for managing the registration process.
                     This will help us avoid flurry of email exchanges. Here are the steps that you have to follow:
                     <br /><br />


                        1. Go to: <a href="https://cmt3.research.microsoft.com/IOL2016" target="blank">
                           <b>https://cmt3.research.microsoft.com/IOL2016</b></a>

                        <br /><br />

                        2. If you already have a login for the CMT system,
                           you can login using your existing credentials, else,
                           register as a new user.<b> Each country should create ONLY ONE
                           login through which the registration process of the country will be handled.</b>
                           Ideally, only one person is expected to handle the process on behalf of the country.
                           However, if you want, you can share the login and password for your country with a few other people
                           and coordinate it between yourselves.
                        <br /><br />

                        3. After logging in for the first time, click on “new submission” and you will see the registration form. </li>
                        <br /><br />

                        4. <b>Write the name of the country/territory in the TITLE field</b>, and proceed to answer all the
                           other questions. For certain things, you may not know the answer right away. That is fine. You can
                           login any time you want, and revise the information in the form by clicking on current submissions and
                            then the “edit submission” button. [Do not create a new submission for revision].
                        <br /><br />

                        5. Download the <a href="ParticipantDetails.xlsx"><b>participant detail form here</b></a>. The file has two sheets.
                           Fill in both the sheets. Again it is OK if you do not have all the information right now.
                            Fill in what you know. Ideally, you should be able to complete the first sheet: “General Info”
                            before you want to register. As soon as you fill in the details, the total registration fee due will
                            be immediately displayed.
                        <br /><br />

                        6. You will find a “file upload” button in the submission page.
                           Upload the fully or partially filled participant detail form, and click the submit button.
                        <br /><br />

                        7. Once you submit, we will be able to see your submission, validate the details and
                           <b>send you a <i>proforma invoice</i> with the bank details.</b>
                        <br /><br />

                        8. You make the payment (ONLY through international bank transfer) at your convenience and
                           login to the CMT site again and appropriately update the payment status.
                        <br /><br />

                        9. Once we receive the money at our end, we will confirm you the amount, send the invoice and
                           inform you about any remaining balance that you need to pay.
                        <br /><br />

                        10. When you know the participant names, post IOL-excursion, food and accommodation preferences etc.,
                           complete the second sheet of the excel form, and upload the newly filled file again through the
                           same portal (using the <i>edit submission</i> option). Do not forget to revise the status appropriately,
                           every time you make some update.
                        <br /><br />

                     In case of any trouble or confusion, just shoot an email to <b>iol16@plo-in.org</b>

                  </p>
